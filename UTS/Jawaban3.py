names = ['Kalimantan west', 'Kalimantan north', 'Kalimantan south', 'Sumatra west', 'Sumatra north', 'Sumatra south', 'Sulawesi west', 'Sulawesi north', 'Sulawesi south']
# outer loop
for name in names:
    # inner while loop
    count = 0
    while count < 1:
        print(name, end=' ')
        # increment counter
        count = count + 1
    print()